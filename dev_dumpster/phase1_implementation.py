def create_rank_zscore_features_1187(df, base_features=None):
    """
    Create rank and z-score features based on 1187-feature pipeline specifications.
    """
    print("Creating 1187-feature pipeline rank and z-score features...")
    
    if base_features is None:
        exclude = ['date_id', 'forward_returns', 'risk_free_rate', 'market_forward_excess_returns']
        base_features = [col for col in df.columns if col not in exclude]
    
    rank_cols = []
    zscore_cols = []
    
    for base_col in base_features:
        if base_col in df.columns:
            # Rank features
            rank_col = f"{base_col}_RANK"
            df[rank_col] = df[base_col].rank(method='min')
            rank_cols.append(rank_col)
            
            # Z-score features
            mean_c = df[base_col].mean()
            std_c = df[base_col].std()
            std_c_safe = std_c if abs(std_c) > 1e-6 else 1e-6
            zscore_col = f"{base_col}_ZSCORE"
            df[zscore_col] = (df[base_col] - mean_c) / std_c_safe
            zscore_cols.append(zscore_col)
    
    print(f"Added {len(rank_cols)} rank features and {len(zscore_cols)} z-score features")
    return df, rank_cols, zscore_cols


