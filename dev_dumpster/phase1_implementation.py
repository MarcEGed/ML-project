def create_comprehensive_interaction_terms(df, feature_names):
    """
    Create comprehensive interaction terms for feature expansion.
    """
    print("\n" + "="*70)
    print("1.4: COMPREHENSIVE INTERACTION FEATURE EXPANSION")
    print("="*70)
    
    # Get base features (non-engineered features)
    base_features = [f for f in feature_names if '_' not in f or f.count('_') == 1 and f.split('_')[1].isdigit()]
    base_features = [f for f in base_features if f in df.columns and f not in ['date_id', 'forward_returns', 'risk_free_rate', 'market_forward_excess_returns']]
    
    print(f"Creating interaction terms from {len(base_features)} base features")
    
    interaction_count = 0
    
    # 1. All pairwise products
    print("Creating pairwise products...")
    for i, feat1 in enumerate(base_features[:15]):  # Limit to first 15 for computational efficiency
        for j, feat2 in enumerate(base_features[:15]):
            if i < j:  # Upper triangle only
                if feat1 in df.columns and feat2 in df.columns:
                    col_name = f"{feat1}_prod_{feat2}"
                    df[col_name] = df[feat1] * df[feat2]
                    interaction_count += 1
    
    # 2. All pairwise ratios
    print("Creating pairwise ratios...")
    for i, feat1 in enumerate(base_features[:15]):
        for j, feat2 in enumerate(base_features[:15]):
            if i < j:
                if feat1 in df.columns and feat2 in df.columns:
                    col_name = f"{feat1}_div_{feat2}"
                    df[col_name] = df[feat1] / (df[feat2] + 1e-6)
                    interaction_count += 1
    
    # 3. All pairwise differences
    print("Creating pairwise differences...")
    for i, feat1 in enumerate(base_features[:15]):
        for j, feat2 in enumerate(base_features[:15]):
            if i < j:
                if feat1 in df.columns and feat2 in df.columns:
                    col_name = f"{feat1}_minus_{feat2}"
                    df[col_name] = df[feat1] - df[feat2]
                    interaction_count += 1
    
    print(f"Added {interaction_count} comprehensive interaction features")
    return df


