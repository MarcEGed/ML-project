def create_zscore_features_261(df):
    """
    Create z-score features from 261-feature pipeline.
    """
    print("Creating 261-feature pipeline z-score features...")
    
    # Use rolling std specs for z-score calculation
    eps = 1e-6
    for base_col, windows in config.ROLLING_STD_SPECS_261.items():
        if base_col in df.columns:
            x = df[base_col]
            for window in windows:
                roll_mean = x.rolling(window=window, min_periods=1).mean()
                roll_std = x.rolling(window=window, min_periods=1).std()
                col_name = f"{base_col}_roll_z_{window}"
                df[col_name] = (x - roll_mean) / (roll_std + eps)
    
    print(f"Added z-score features for {len(config.ROLLING_STD_SPECS_261)} base features")
    return df


