def run_shap_analysis(model, X_train, feature_names, model_type="regression"):
    """
    Run SHAP analysis on a trained model to understand feature importance.
    """
    if not SHAP_AVAILABLE:
        print("SHAP not available. Skipping SHAP analysis.")
        return None
    
    print(f"\nRunning SHAP analysis for {model_type} model...")
    
    try:
        # Create SHAP explainer based on model type
        if isinstance(model, lgb.LGBMRegressor) or isinstance(model, lgb.LGBMClassifier):
            explainer = shap.TreeExplainer(model)
        elif isinstance(model, (RandomForestRegressor, RandomForestClassifier)):
            explainer = shap.TreeExplainer(model)
        elif isinstance(model, (Ridge, LogisticRegression)):
            explainer = shap.LinearExplainer(model, X_train)
        else:
            print(f"No SHAP explainer available for model type: {type(model)}")
            return None
        
        # Calculate SHAP values
        if len(X_train) > 1000:  # Use sample for large datasets
            X_sample = X_train[:1000]
            print(f"Using sample of {len(X_sample)} instances for SHAP calculation")
        else:
            X_sample = X_train
        
        shap_values = explainer.shap_values(X_sample)
        
        # For binary classification, shap_values might be a list
        if isinstance(shap_values, list):
            shap_values = shap_values[0] if len(shap_values) > 0 else shap_values
        
        # Create SHAP summary
        shap_df = pd.DataFrame({
            'feature': feature_names,
            'shap_importance': np.abs(shap_values).mean(axis=0)
        }).sort_values('shap_importance', ascending=False)
        
        print(f"SHAP analysis complete. Top 10 features:")
        print(shap_df.head(10).to_string(index=False))
        
        return shap_df
        
    except Exception as e:
        print(f"Error in SHAP analysis: {e}")
        return None


def compare_feature_importance_across_models(X_train, y_train, feature_names):
    """
    Compare feature importance across different model types.
    """
    print("\n" + "="*70)
    print("1.3: FEATURE IMPORTANCE ANALYSIS ACROSS MODELS")
    print("="*70)
    
    models_to_test = [
        ("LightGBM", lgb.LGBMRegressor(**config.LGBM_PARAMS)),
        ("Ridge", Ridge(alpha=500.0, random_state=config.RANDOM_STATE)),
        ("RandomForest", RandomForestRegressor(n_estimators=100, random_state=config.RANDOM_STATE)),
    ]
    
    all_importances = {}
    
    for model_name, model in models_to_test:
        print(f"\nTraining {model_name} model...")
        model.fit(X_train, y_train)
        
        # Get feature importances from the model
        if hasattr(model, 'feature_importances_'):
            model_importances = model.feature_importances_
        elif hasattr(model, 'coef_'):
            model_importances = np.abs(model.coef_)
        else:
            print(f"No feature importance available for {model_name}")
            continue
        
        importance_df = pd.DataFrame({
            'feature': feature_names,
            'importance': model_importances
        }).sort_values('importance', ascending=False)
        
        all_importances[model_name] = importance_df
        
        print(f"{model_name} - Top 10 features:")
        print(importance_df.head(10).to_string(index=False))
        
        # Run SHAP analysis for this model
        if SHAP_AVAILABLE:
            shap_df = run_shap_analysis(model, X_train, feature_names, model_name)
            if shap_df is not None:
                all_importances[f"{model_name}_SHAP"] = shap_df
    
    # Identify overlapping high-importance features
    print(f"\n" + "="*70)
    print("OVERLAPPING HIGH-IMPORTANCE FEATURES")
    print("="*70)
    
    # Get top 20 features from each model
    top_features_by_model = {}
    for model_name, importance_df in all_importances.items():
        if not model_name.endswith('_SHAP'):
            top_20 = importance_df['feature'].head(20).tolist()
            top_features_by_model[model_name] = set(top_20)
    
    # Find intersection of top features across models
    common_top_features = set.intersection(*top_features_by_model.values())
    print(f"Features in top 20 for ALL models: {len(common_top_features)}")
    print(f"Common features: {sorted(list(common_top_features))[:20]}")  # Limit to first 20
    
    # Create feature importance hierarchy
    print(f"\nCreating feature importance hierarchy...")
    all_features = set()
    for importance_df in all_importances.values():
        all_features.update(importance_df['feature'].tolist())
    
    # Calculate average rank across models
    feature_ranks = {}
    for feature in all_features:
        ranks = []
        for model_name, importance_df in all_importances.items():
            if not model_name.endswith('_SHAP'):
                try:
                    rank = importance_df[importance_df['feature'] == feature].index[0]
                    ranks.append(rank)
                except (IndexError, KeyError):
                    ranks.append(len(importance_df))  # Assign worst rank if not found
        feature_ranks[feature] = np.mean(ranks)
    
    hierarchy_df = pd.DataFrame({
        'feature': list(feature_ranks.keys()),
        'avg_rank': list(feature_ranks.values())
    }).sort_values('avg_rank')
    
    print("Feature Importance Hierarchy (by average rank):")
    print(hierarchy_df.head(20).to_string(index=False))
    
    return all_importances, hierarchy_df


