    initial_len = len(df)
    df = df.dropna()
    print(f"Dropped {initial_len - len(df)} rows with missing values")
    
    # Create volatility proxy
    v_cols = [col for col in df.columns if col.startswith('V')]
    if v_cols:
        df['vol_proxy'] = df[v_cols].abs().mean(axis=1)
